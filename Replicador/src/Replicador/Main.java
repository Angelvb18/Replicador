    public void CompararBases(JTable jTable,JTable jTable1) {
        try {
            // Obtén la lista de tablas no existentes en MSSQL
            List<String> tablasNoExistentesEnMSSQL = mssql_c.compararTablas(infoBase2[1], mariadb_c);

            // Crea un modelo de tabla para manejar los datos en la JTable
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Elementos No Replicados");
            DefaultTableModel model2 = new DefaultTableModel();
            model2.addColumn("Elementos Replicados");
            // Agrega las tablas a la tabla
            for (String tabla : tablasNoExistentesEnMSSQL) {
                model.addRow(new Object[]{tabla});
            }

            // Establece el modelo de tabla en la JTable
            jTable.setModel(model);
            jTable1.setModel(model2);
        } catch (Exception ex) {
            System.err.println("Error al comparar bases y llenar la JTable: " + ex.getMessage());
        }
    }
    

