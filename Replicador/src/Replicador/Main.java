public String construirQueryCrearTabla(Tabla tabla) {
    StringBuilder query = new StringBuilder();
    query.append("CREATE TABLE ").append(tabla.getNombre()).append(" (");

    for (Columna columna : tabla.getColumnas()) {
        query.append(columna.getNombre()).append(" ").append(columna.getTipoDato());

        // Agregar la longitud si es un tipo de dato que lo permite
        if (columna.getLongitudMaxima() > 0) {
            query.append("(").append(columna.getLongitudMaxima()).append(")");
        }

        // Agregar la restricción PRIMARY KEY si es clave primaria
        if (columna.esClavePrimaria()) {
            query.append(" PRIMARY KEY");
        }

        query.append(", ");
    }

    // Eliminar la coma extra al final y cerrar paréntesis
    query.delete(query.length() - 2, query.length()).append(");");

    return query.toString();
}


